// Helper function to generate product IDs
export function generateProductIds(products: typeof seafoodProducts): Product[] {
  return products.map((product, index) => ({
    ...product,
    id: `seafood_${String(index + 1).padStart(3, '0')}`,
    created_at: new Date(),
    updated_at: new Date()
  }));
}

// Helper function to generate category IDs
export function generateCategoryIds(categories: typeof seafoodCategories): Category[] {
  return categories.map((category, index) => ({
    ...category,
    id: `cat_seafood_${String(index + 1).padStart(2, '0')}`,
    created_at: new Date(),
    updated_at: new Date()
  }));
}